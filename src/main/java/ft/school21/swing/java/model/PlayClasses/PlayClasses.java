package ft.school21.swing.java.model.PlayClasses;

public class PlayClasses {
    private int classAttack;
    private String className;
    private int classDefense;

    public int getClassAttack() {
        return classAttack;
    }

    public void setClassAttack(int classAttack) {
        this.classAttack = classAttack;
    }

    public String getClassName() {
        return className;
    }

    public void setClassName(String className) {
        this.className = className;
    }

    public int getClassDefense() {
        return classDefense;
    }

    public void setClassDefense(int classDefense) {
        this.classDefense = classDefense;
    }

}
